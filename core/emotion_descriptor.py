# 次要情绪着色短语（当某维度为次要情绪且 > 0.15 时使用）
_SECONDARY_PHRASES: dict[str, str] = {
    "anger":        "心里有口气",
    "fear":         "悬着一点",
    "joy":          "带着一点轻盈",
    "sadness":      "有些沉",
    "surprise":     "还没完全回过神",
    "disgust":      "有些反感",
    "anticipation": "带着一点期盼",
    "trust":        "有点依赖",
}


def get_emotion_description(emotion_dict: dict) -> str:
    """
    将情绪向量转换为文字描述，供注入 LLM prompt。

    输出格式：
      主导情绪（名称）：{完整区间描述}
      情绪底色：{次要短语1}，{次要短语2}    ← 仅当有次要情绪 > 0.15 时出现

    edge cases：
      - 全维度 ≤ 0.05 → "情绪平稳，没有明显的情绪波动"
      - 次要情绪最多取 2 个（按值降序）
    """
    values = {
        k: v for k, v in emotion_dict.items()
        if k != "intensity" and k in _DESCRIPTORS
    }
    if not values or max(values.values()) <= 0.05:
        return "情绪平稳，没有明显的情绪波动"

    dominant = max(values, key=values.get)
    interval = min(int(values[dominant] * 10), 9)
    dominant_desc = _DESCRIPTORS[dominant][interval]

    secondary = sorted(
        [(k, v) for k, v in values.items() if k != dominant and v > 0.15],
        key=lambda x: x[1],
        reverse=True,
    )[:2]

    result = f"主导情绪（{_ZH_NAMES[dominant]}）：{dominant_desc}"
    if secondary:
        tints = [_SECONDARY_PHRASES[k] for k, _ in secondary if k in _SECONDARY_PHRASES]
        if tints:
            result += f"\n情绪底色：{'，'.join(tints)}"
    return result
